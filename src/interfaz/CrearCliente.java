    public void crearCliente() {
        if (StringUtils.isEmpty(NombreTxt.getText())) {
            JOptionPane.showMessageDialog(null, "Indique Nombre", "Error de Inserci贸n", JOptionPane.ERROR_MESSAGE);
        }
        if (StringUtils.isEmpty(DocumentoTxt.getText())) {
            JOptionPane.showMessageDialog(null, "Indique Documento", "Error de Inserci贸n", JOptionPane.ERROR_MESSAGE);
        }
        if (StringUtils.isEmpty(CorreoTxt.getText())) {
            JOptionPane.showMessageDialog(null, "Indique Correo", "Error de Inserci贸n", JOptionPane.ERROR_MESSAGE);
        }
        String tipodoc = "";
        if (CedFisicaRB.isSelected()) {
            tipodoc = "01";
        } else if (CedJuridicaRB.isSelected()) {
            tipodoc = "02";
        } else if (DimexRB.isSelected()) {
            tipodoc = "03";
        } else if (NiteRB.isSelected()) {
            tipodoc = "04";
        } else {
            JOptionPane.showMessageDialog(null, "Indique Tipo Documento", "Error de Inserci贸n", JOptionPane.ERROR_MESSAGE);
        }

        codigo = oraclebd.crearCliente(NombreTxt.getText(), tipodoc, DocumentoTxt.getText(), null,
                TelefonosTxt.getText(), DireccionTxt.getText(), "N", CorreoTxt.getText(),
                "", "S", "N");
        this.dispose();
    }

    public int getCodigo() {
        return codigo;
    }

