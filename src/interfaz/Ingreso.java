    private void valida_usuario() {
        String resultado_v = oraclebd.valida_usuario(jTextField1.getText(), jPasswordField1.getText());
        if (resultado_v.equals("N")) {
            JOptionPane.showMessageDialog(null, "Usuario o Clave Invalida", "Error de Ingreso", JOptionPane.ERROR_MESSAGE);
        } else {
            JFrame Menu = new Menu(oraclebd, jTextField1.getText().toUpperCase());
            Menu.setVisible(true);
            this.dispose();
        }
    }

